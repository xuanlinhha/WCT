    private void centerWindow(Window frame) {
        Dimension dimension = Toolkit.getDefaultToolkit().getScreenSize();
        int x = (int) ((dimension.getWidth() - frame.getWidth()) / 2);
        int y = (int) ((dimension.getHeight() - frame.getHeight()) / 2);
        frame.setLocation(x, y);
    }

    private void setPanel(int panelId) {
        jPanel1.removeAll();
        jPanel1.repaint();
        jPanel1.revalidate();
        switch (panelId) {
            case 0: {
                jPanel1.add(jPanel2);
                jPanel1.repaint();
                jPanel1.revalidate();
                this.setTitle("Generate copies - Send files");
                break;
            }
            case 1: {
                jPanel1.add(jPanel3);
                jPanel1.repaint();
                jPanel1.revalidate();
                this.setTitle("Detect mouse position");
                break;
            }
            case 2: {
                jPanel1.add(jPanel4);
                jPanel1.repaint();
                jPanel1.revalidate();
                this.setTitle("All config");
                break;
            }
            default:
                break;

        }
    }
