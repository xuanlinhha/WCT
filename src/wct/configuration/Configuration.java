package wct.configuration;

/**
 *
 * @author xuanlinhha
 */
public class Configuration {

    private String inputFolder;
    private String outputFolder;
    private String onTaskbarCoordinate;
    private String selectedColor;
    private String imageCoordinate1;
    private String imageCoordinate2;

    public String getInputFolder() {
        return inputFolder;
    }

    public void setInputFolder(String inputFolder) {
        this.inputFolder = inputFolder;
    }

    public String getOutputFolder() {
        return outputFolder;
    }

    public void setOutputFolder(String outputFolder) {
        this.outputFolder = outputFolder;
    }

    public String getOnTaskbarCoordinate() {
        return onTaskbarCoordinate;
    }

    public void setOnTaskbarCoordinate(String onTaskbarCoordinate) {
        this.onTaskbarCoordinate = onTaskbarCoordinate;
    }

    public String getSelectedColor() {
        return selectedColor;
    }

    public void setSelectedColor(String selectedColor) {
        this.selectedColor = selectedColor;
    }

    public String getImageCoordinate1() {
        return imageCoordinate1;
    }

    public void setImageCoordinate1(String imageCoordinate1) {
        this.imageCoordinate1 = imageCoordinate1;
    }

    public String getImageCoordinate2() {
        return imageCoordinate2;
    }

    public void setImageCoordinate2(String imageCoordinate2) {
        this.imageCoordinate2 = imageCoordinate2;
    }

}
